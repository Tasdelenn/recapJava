public class DortIslem {
    //Basit Hesap Makinesi

    public int sum(int formalNum1, int formalNum2){
        return formalNum1+formalNum2;
    }

    public int minus(int formalNum1, int formalNum2){
        return formalNum1-formalNum2;
    }

    public int multiply(int formalNum1, int formalNum2){
        return formalNum1*formalNum2;
    }

    public double devide(int formalNum1, int formalNum2){
        return (double) formalNum1/formalNum2;
    }

}
